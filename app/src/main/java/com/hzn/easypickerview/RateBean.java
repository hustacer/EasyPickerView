package com.hzn.easypickerview;

public class RateBean {
    public String rateIconUrl;
    public String rateCountry;
    public String rateContent;

    public String getRateIconUrl() {
        return rateIconUrl;
    }

    public void setRateIconUrl(String rateIconUrl) {
        this.rateIconUrl = rateIconUrl;
    }

    public String getRateCountry() {
        return rateCountry;
    }

    public void setRateCountry(String rateCountry) {
        this.rateCountry = rateCountry;
    }

    public String getRateContent() {
        return rateContent;
    }

    public void setRateContent(String rateContent) {
        this.rateContent = rateContent;
    }
}